import React, { useEffect, useState } from 'react';
import './App.css';

interface Wish {
  id: number;
  text: string;
  forWhom: string;
  priority: string;
  comment: string;
  done: boolean;
}

type Theme = 'auto' | 'light' | 'dark' | 'pastel';

function App() {
  const [wishes, setWishes] = useState<Wish[]>(() => {
    const saved = localStorage.getItem('wishes');
    return saved ? JSON.parse(saved) : [];
  });

  const [text, setText] = useState('');
  const [forWhom, setForWhom] = useState('–≤–¥–≤–æ–µ–º');
  const [priority, setPriority] = useState('–æ–±—ã—á–Ω–æ–µ');
  const [comment, setComment] = useState('');

  // –Ω–æ–≤—ã–µ –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ UI
  const [theme, setTheme] = useState<Theme>(() => (localStorage.getItem('theme') as Theme) || 'auto');
  const [compact, setCompact] = useState<boolean>(() => localStorage.getItem('compact') === '1');

  // —Å–æ—Ö—Ä–∞–Ω—è–µ–º –∂–µ–ª–∞–Ω–∏—è
  useEffect(() => {
    localStorage.setItem('wishes', JSON.stringify(wishes));
  }, [wishes]);

  // –ø—Ä–∏–º–µ–Ω—è–µ–º —Ç–µ–º—É
  useEffect(() => {
    const root = document.documentElement;
    if (theme === 'auto') {
      root.removeAttribute('data-theme');
    } else {
      root.setAttribute('data-theme', theme);
    }
    localStorage.setItem('theme', theme);
  }, [theme]);

  // –ø—Ä–∏–º–µ–Ω—è–µ–º –∫–æ–º–ø–∞–∫—Ç–Ω—ã–π —Ä–µ–∂–∏–º
  useEffect(() => {
    document.body.classList.toggle('compact', compact);
    localStorage.setItem('compact', compact ? '1' : '0');
  }, [compact]);

  const handleAdd = () => {
    if (!text.trim()) return;
    const newWish: Wish = {
      id: Date.now(),
      text,
      forWhom,
      priority,
      comment,
      done: false,
    };
    setWishes([newWish, ...wishes]);
    setText('');
    setComment('');
  };

  const toggleDone = (id: number) => {
    setWishes(wishes.map(wish =>
      wish.id === id ? { ...wish, done: !wish.done } : wish
    ));
  };

  const clearAll = () => {
    if (window.confirm('–£–¥–∞–ª–∏—Ç—å –≤—Å–µ –∂–µ–ª–∞–Ω–∏—è?')) {
      setWishes([]);
      localStorage.removeItem('wishes');
    }
  };

  return (
    <div className="container">
      <h1 className="title">üíñ –ù–∞—à–∞ –¥–æ—Å–∫–∞ –∂–µ–ª–∞–Ω–∏–π</h1>

      {/* –ü–∞–Ω–µ–ª—å –Ω–∞—Å—Ç—Ä–æ–µ–∫ */}
      <div className="toolbar">
        <label className="toolbar-item">
          –¢–µ–º–∞:
          <select value={theme} onChange={(e) => setTheme(e.target.value as Theme)}>
            <option value="auto">–ê–≤—Ç–æ</option>
            <option value="light">–°–≤–µ—Ç–ª–∞—è</option>
            <option value="dark">–¢—ë–º–Ω–∞—è</option>
            <option value="pastel">–ü–∞—Å—Ç–µ–ª—å</option>
          </select>
        </label>

        <label className="toolbar-item switch">
          <input
            type="checkbox"
            checked={compact}
            onChange={(e) => setCompact(e.target.checked)}
          />
          <span>–ö–æ–º–ø–∞–∫—Ç–Ω–æ</span>
        </label>
      </div>

      {/* –§–æ—Ä–º–∞ */}
      <div className="form">
        <input
          placeholder="–ß—Ç–æ —Ç—ã —Ö–æ—á–µ—à—å —Å–¥–µ–ª–∞—Ç—å?"
          value={text}
          onChange={(e) => setText(e.target.value)}
        />
        <textarea
          placeholder="–ö–æ–º–º–µ–Ω—Ç–∞—Ä–∏–π (–ø–æ –∂–µ–ª–∞–Ω–∏—é)"
          value={comment}
          onChange={(e) => setComment(e.target.value)}
        />
        <div className="form-row">
          <select value={forWhom} onChange={(e) => setForWhom(e.target.value)}>
            <option value="–≤–¥–≤–æ–µ–º">–í–¥–≤–æ–µ–º</option>
            <option value="–¥–ª—è –Ω–µ–µ">–î–ª—è –Ω–µ—ë</option>
            <option value="–¥–ª—è –Ω–µ–≥–æ">–î–ª—è –Ω–µ–≥–æ</option>
          </select>
          <select value={priority} onChange={(e) => setPriority(e.target.value)}>
            <option value="–æ–±—ã—á–Ω–æ–µ">–û–±—ã—á–Ω–æ–µ</option>
            <option value="–≤–∞–∂–Ω–æ–µ">–í–∞–∂–Ω–æ</option>
            <option value="–º–µ—á—Ç–∞">–ú–µ—á—Ç–∞ üí´</option>
          </select>
          <button onClick={handleAdd}>–î–æ–±–∞–≤–∏—Ç—å</button>
        </div>
      </div>

      {wishes.length > 0 && (
        <div className="wishes">
          {wishes.map(wish => (
            <div
              key={wish.id}
              className={`card ${wish.done ? 'done' : ''} priority-${wish.priority}`}
            >
              <div className="card-header">
                <div className="card-text">{wish.text}</div>
                <button className="done-btn" onClick={() => toggleDone(wish.id)}>
                  {wish.done ? '‚Ü©Ô∏è' : '‚úÖ'}
                </button>
              </div>
              <div className="card-meta">üéØ {wish.forWhom}</div>
              {wish.comment && <div className="card-comment">üí¨ {wish.comment}</div>}
            </div>
          ))}
          <button className="clear-btn" onClick={clearAll}>üóë –û—á–∏—Å—Ç–∏—Ç—å –≤—Å—ë</button>
        </div>
      )}
    </div>
  );
}

export default App;
